from torch import nn
import torch.nn.functional as F


class ResidualBlock1D(nn.Module):
    def __init__(self, inchannel, outchannel, stride=1, dilation=1):
        super(ResidualBlock1D, self).__init__()
        self.left = nn.Sequential(
            nn.Conv1d(inchannel, outchannel, kernel_size=3, stride=stride, padding=1, bias=False),
            nn.BatchNorm1d(outchannel),
            nn.ReLU(inplace=True),
            nn.Conv1d(outchannel, outchannel, kernel_size=3, stride=1, padding=dilation, dilation=dilation, bias=False),
            nn.BatchNorm1d(outchannel)
        )

        self.shortcut = nn.Sequential()
        if stride != 1 or inchannel != outchannel:
            self.shortcut = nn.Sequential(
                nn.Conv1d(inchannel, outchannel, kernel_size=1, stride=stride, bias=False),
                nn.BatchNorm1d(outchannel)
            )

    def forward(self, x):
        out = self.left(x)

        out = out + self.shortcut(x)
        out = F.relu(out)

        return out


class ResidualBlock(nn.Module):
    def __init__(self, inchannel, outchannel, stride=1, dilation=1, attention=None):
        super(ResidualBlock, self).__init__()
        self.attention = attention
        self.left = nn.Sequential(
            nn.Conv2d(inchannel, outchannel, kernel_size=3, stride=stride, padding=1, bias=False),
            nn.BatchNorm2d(outchannel),
            nn.ReLU(inplace=True),
            nn.Conv2d(outchannel, outchannel, kernel_size=3, stride=1, padding=dilation, dilation=dilation, bias=False),
            nn.BatchNorm2d(outchannel)
        )

        self.se = SELayer(outchannel, outchannel // 2)
        self.simam = SimAM()

        self.shortcut = nn.Sequential()
        if stride != 1 or inchannel != outchannel:
            self.shortcut = nn.Sequential(
                nn.Conv2d(inchannel, outchannel, kernel_size=1, stride=stride, bias=False),
                nn.BatchNorm2d(outchannel)
            )

    def forward(self, x):
        out = self.left(x)

        if self.attention == 'se':
            out = self.se(out)
        if self.attention == 'simam':
            out = self.simam(out)

        out = out + self.shortcut(x)
        out = F.relu(out)

        return out


